#Escreva um programa para calcular a área de um triângulo, sendo dados a sua base e a sua altura

base = float(input("digitar a base do triangulo (em metros):"))
altura = float(input("digitar a altura do triangulo ( em metros)"))

area = (base * altura) /2

print(f"a area do triangulo e: {area:.2f} ao quadrado")
